                                                     </a>
                                                   </h4>
                                                 </div>
                                                 <div id="collapseThree" class="panel-collapse collapse">
                                                   <div class="panel-body">
                                                    A car AC blowing out hot air can be due to low refrigerant level or due to a problem in the condenser, the compressor, the cooling fan, or the electrical system of your car. It can also be a restricted condenser coil. In such a case, it is highly advised to get your car thoroughly inspected at the nearest Superior Honda workshop in  Mumbai .
