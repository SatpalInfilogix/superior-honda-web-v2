                                                   </div>
                                                   <div id="collapsesix" class="panel-collapse collapse">
                                                     <div class="panel-body">
                                                        Low airflow from the AC blower can be an issue with the evaporator coil (inside cooling coil) or a dirty air filter, clogged drainpipe, or a broken fan blower inside your car AC vent. To avoid further deterioration, we highly advise you to get your car AC thoroughly diagnosed at the nearest Superior Honda workshop in Mumbai
