                                                   </h4>
                                                 </div>
                                                 <div id="collapseOne" class="panel-collapse collapse in">
                                                   <div class="panel-body">
                                                    Ideally, you should get the AC on your car serviced at least once a year or when you feel that the AC is not cooling as it used to. Simple enough! If you start noticing the AC isn’t reaching the optimum cooling temperature, have your system inspected. In most situations, the car is diagnosed with low or leaking AC gas.
